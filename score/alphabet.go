package score

type Letter struct {
	Value     byte
	Frequency float64
}

type Alphabet []Letter

func (a Alphabet) Contains(value byte) bool {
	for _, letter := range a {
		if letter.Value == value {
			return true
		}
	}
	return false
}

func (a Alphabet) Frequency(value byte) float64 {
	for _, letter := range a {
		if letter.Value == value {
			return letter.Frequency
		}
	}
	return 0
}

func (a Alphabet) Len() int {
	return len(a)
}

func (a Alphabet) Swap(i, j int) {
	a[i], a[j] = a[j], a[i]
}

func (a Alphabet) Less(i, j int) bool {
	return a[i].Frequency > a[j].Frequency
}

